#include "libft.h"

void			*ft_memset(void *s, int c, size_t n)
{
	char		*tmp;
	size_t		i;
	int			bol;

	tmp = (char*)s;
	i = 0;
	bol = 0;
	while (i < n)
	{
		tmp[i] = c;
		i++;
	}
	return (s);
}
