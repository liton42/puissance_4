#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	o;

	i = 0;
	o = (char)c;
	while (s[i] != o)
	{
		if (s[i] == '\0')
			return (NULL);
		i++;
	}
	return ((char*)s + i);
}
